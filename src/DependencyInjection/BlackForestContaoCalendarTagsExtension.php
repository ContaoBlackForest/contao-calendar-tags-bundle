namespace BlackForest\Contao\Calendar\Tags\DependencyInjection;

use Symfony\Component\Config\FileLocator;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Extension\Extension;
use Symfony\Component\DependencyInjection\Loader\YamlFileLoader;

/**
 * This is the Bundle extension.
 */
class BlackForestContaoCalendarTagsExtension extends Extension
{
    /**
     * {@inheritDoc}
     */
    public function load(array $configs, ContainerBuilder $container)
    {
        $loader = new YamlFileLoader($container, new FileLocator(__DIR__ . '/../Resources/config'));
        $loader->load('module/calendar-events-detail.yml');
        $loader->load('module/calendar-events-list.yml');
        $loader->load('table/calendar-events.yml');
        $loader->load('table/calendar.yml');
        $loader->load('table/calendar-events-tags.yml');
        $loader->load('table/calendar-events-tags-relation.yml');
        $loader->load('services.yml');
    }
}
